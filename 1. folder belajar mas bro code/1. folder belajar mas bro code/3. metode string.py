# Contoh beberapa metode string
"""
contoh dibawah ini dapat membantu anda kedepannya untuk project project :)
"""

nama = "Misbahudin Ramadhani"
print()

# Mengetahui panjang string
print(len(nama))
print()

# Mencari sesuatu dalam variabel string
print(nama.find("a"))
print()

# Membuat huruf pertama string menjadi kapital
print(nama.capitalize())
print()

# Membuat semua huruf menjadi kapital
print(nama.upper())
print()

# Membuat semua huruf menjadi kecil
print(nama.lower())
print()

# Apakah variabel berisi hanya angka (bulat)
print(nama.isdigit())
print()

# Apakah variabel berisi hanya huruf
print(nama.isalpha())
print()

# Menghitung berapa jumalah sesuatu dalam variabel string
print(nama.count("a"))
print()

# Mengganti suatu dengan yang lain
print(nama.replace("a", "u"))
print()

# Memperbanyak string dalam variabel
print(nama*10)